DELETE FROM UPCOM A
WHERE ROWID > (SELECT MIN(ROWID) FROM UPCOM B
WHERE B.MA = A.MA AND B.NGAY = A.NGAY);

DELETE FROM HNX A
WHERE ROWID > (SELECT MIN(ROWID) FROM HNX B
WHERE B.MA = A.MA AND B.NGAY = A.NGAY);

DELETE FROM HOSE A
WHERE ROWID > (SELECT MIN(ROWID) FROM HOSE B
WHERE B.MA = A.MA AND B.NGAY = A.NGAY);

DELETE FROM CDO."INDEX" A
WHERE ROWID > (SELECT MIN(ROWID) FROM CDO."INDEX" B
WHERE B.CHISO = A.CHISO AND B.NGAY = A.NGAY);